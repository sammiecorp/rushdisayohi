<h1>Шириншоҳ Шоҳтемур</h1>
<p>Шириншоҳ Шоҳтемур — арбоби сиёсию давлатии Тоҷикистон, Қаҳрамони Тоҷикистон, яке аз асосгузорони Тоҷикистони Шӯравӣ.
<br>
Шириншоҳ Шоҳтемур фарзанди деҳқон аз деҳаи Поршиневи волости Шуғнон (Помири ғарбӣ) аморати Бухоро аст. Ӯ соли 1899 ба дунё омадааст. Волидонаш ба ғаллакорӣ шуғл варзида зиндагии хеле қашшоққона доштанд. Тахминан соли 1908 ё соли 1909, вақте Шириншоҳ ки 8-9 сола буд, волидонаш аз олам гузаштанд. Аслу насаби падараш аҳли рӯҳонист, ки он дар номи насаби ӯ низ акс ёфтааст. Маънои ҳиссачаи «шо» – саид, ё худ вориси бевоситаи Муҳаммад аст. То дувоздаҳсолагӣ дар назди бародараш умр ба сар бурдааст. Аз соли 1911) ба интернати назди омӯзишгоҳи русӣ-маҳаллии Хоруғ, ки дар Кӯҳистони Бадахшон бори аввал кушода шуда буд, дохил гардида, дар он 4 сол аз ҳисоби давлати русҳо хондааст. Зимистон таҳсил дошта, тобистон дар назди сардори отряди Помир кор мекард – гулҳоро об медод. Аз 14-солагӣ бо меҳнати хеш зиндагӣ мекард.</p>
