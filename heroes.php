    <h1>Эмомалӣ Раҳмон</h1>
    <p>Эмомалӣ Раҳмон 5 октябри соли 1952 дар ноҳияи Данғараи Ҷумҳурии Тоҷикистон дар оилаи деҳқон таваллуд шудааст. <br>

Фаъолияти меҳнатиашро соли 1969, баъд аз хатми Омӯзишгоҳи касбию техникии № 40-и шаҳри Калининобод (ҳоло Сарбанд), ба ҳайси устои барқ дар Корхонаи равғанкашии шаҳри Қӯрғонтеппа шурӯъ намудааст. Солҳои 1971-1974 дар Флоти уқёнуси Ором дар хизмати ҳарбӣ буд. Пас аз хизмати ҳарбӣ ба зодгоҳи худ баргашта, дар совхози ба номи Ленини ноҳияи Данғара кор кардааст. Соли 1982 факултети иқтисоди Донишгоҳи миллии Тоҷикистонро хатм намуд.
<br>
Солҳои 1976-1987 дар вазифаҳои котиби раёсат, раиси Кумитаи иттифоқҳои касабаи совхози ба номи Ленини ноҳияи Данғара буд. Сипас дар мақомоти ҳизбӣ фаъолият дошт.</p>
  <br><hr>
  <h1>Мирзо Турсунзода</h1>
  <p>Мирзо Турсунзода (2 майи 1911, Қаратоғ, Бекигарии Ҳисор, Аморати Бухоро — 24 сентябри 1977, Душанбе, ҶШС Тоҷикистон, ИҶШС) — шоир, арбоби ҷамъиятиву сиёсӣ, раиси Иттифоқи нависандагони Тоҷикистон, Шоири халқии Тоҷикистон (1961), академики АИ ҶШС Тоҷикистон (1951), Қаҳрамони меҳнати сотсиалистӣ (1967). Раиси Кумитаи якдилии халқҳои Осиё ва Африқо. Барандаи Ҷоизаи байналмилалии ба номи Неҳру, Ҷоизаи давлатии СССР (ИҶШС) ва Ҷоизаи давлатии Тоҷикистон ба номи Абӯабдуллоҳи Рӯдакӣ. Қаҳрамони Тоҷикистон (2001).
<br>
Мирзо Турсунзода 2 майи соли 1911 дар деҳаи хушманзараи Қаратоғ водии Ҳисор ба дунё омадааст. Падараш, усто Турсун яке аз ҳунармандони гулдасти ин води (деҳа) буд. Бо вуҷуди ин зиндагонии аҳли оилаи у қашоқона мегузашт. Ба ин нигоҳ накарда, усто Турсун мехост, ки писараш Мирзо соҳиби хату савод шавад. Мирзои хурдсол чанде дар назди муллои деҳа таҳсил кард. Баъди ғалабаи Инқилоби Октябри Кабир, ба рӯи мардуми камбағал дари бахту саодат ва илму маърифат кушод. Дере нагузашта дар Қаратоғ мактаби нави советӣ ташкил ёфт ва Мирзо аз ҷумлаи аввалин талабагони он гардид. Дар солҳои 1925-1926 дар интернати Душанбе таҳсили худро давом медиҳад.</p>
