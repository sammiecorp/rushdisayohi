    <h1>Садриддин Айнӣ</h1>
    <p>Садриддин Айнӣ — бунёдгузори адабиёти шуравии тоҷик, нависанда, олим, академик ва нахустин Президенти Академияи илмҳои Тоҷикистон (1951–1954), Ходими хизматнишондодаи илми ҶШС Тоҷикистон, академики фахрии Академияи илмҳои ҶШС Ӯзбекистон. Қаҳрамони Тоҷикистон (1997). <br>Сардафтари адабиёти навини тоҷик ва Қаҳрамони миллии Тоҷикистон Садриддин Сайидмуродзода Айнӣ 27 (15) апрел 1878 дар деҳаи Соктареи тумани Ғиждувони Аморати Бухоро (дар ноҳияи Ғиждувони вилояти Бухорои Ўзбекистон) ба дунё омадааст. Фарзандаш ховаршиноси барҷастаи Тоҷикистон Айнӣ Камол. Падараш Саидмуродхоҷа кишоварз буд, аммо чун худ соҳиби хату савод буд, кӯшиш мекард, ки фарзандонаш низ донишомӯхтаву донишманд бошанд. Аз ин рӯ, писараш Садриддин Айниро дар шашсолагӣ (6) ба мадрасаи рустояшон дод. Устод Айнӣ донишомӯзияшро дар он мадраса дар достони «Мактаби кӯҳна» тасвир карда ва дар «Ёддоштҳо» мегӯяд: «Чунон ки падарам дид, дар он мактаб ман саводи амиқ гирифта натавонистам ва маро ба мактаби духтарона дод». Дар мактаби духтарона Садриддин чанд ҷузъ аз Бедил ва чанд ҷузъ аз ғазалиёти Соибро хонда дар даҳсолагӣ мактабро хатм мекунад. Аммо бачагии Садриддин Айнӣ дар оғӯши падару модар дер давом накард. Вабое, ки соли 1889 Бухоро ва атрофи онро фаро гирифт, дар чиҳил рӯз ӯро ҳам аз падар ва ҳам аз модар ҷудо кард. <br>Орзую ҳаваси илму шеър Айниро дар 12-солагӣ ба Бухоро овард. Ба мадрасаҳои Бухоро дохил шудан ва дониш гирифтан барои Айнӣ барин фақирзодагони илмҷӯ хеле мушкил буд. Вале ӯ ба шарофати ҳавасмандӣ ва толиби илм буданаш тамоми душвориҳои рӯзгор ва омӯзишро паси сар карда, 16 сол дар мадрасаҳои Бухоро таҳсил намудааст. Айнӣ дар мадрасаҳои мири Араб, Олимҷон, Бадалбек, Ҳоҷӣ Зоҳид ва Кӯкалдош таҳсил карда, соли 1908 онро хатм мекунад.</p>
