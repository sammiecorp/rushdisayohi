<h1>Нусратулло Махсум</h1>
<p>Нусратулло Махсум — яке аз асосгузорони Тоҷикистони Шӯравӣ ҶШС Тоҷикистон, арбоби сиёсию давлатии Тоҷикистон, сиёсатмадор, дорандаи унвони олии Ҷумҳурии Тоҷикистон — Қаҳрамони Тоҷикистон,
<br>
Нусратулло Махсум (Лутфуллоев) – соли 1881 дар деҳаи Чашмаи Қозии бекигарии Қаротегин (Аморати Бухоро) дар оилаи камбағал таваллуд ёфтааст. Дар оғози ҷавонӣ ба шаҳрҳои Қуқанд ва Фарғонаи кишвари Туркистон сафар карда, чун ҳаммол кор кардааст. Дар давраи инқилоби якуми Русия, солҳои 1905-07 Нусратулло Махсум дар корпартоиҳо ва намоишҳои коргарони шаҳри Қуқанд ширкат варзидааст. Соли 1920 баъди инқилоби халқӣ – демократии Бухоро ва аз тахт афтодани амири Бухоро ба қатори муборизони роҳи таҳкими Ҳокимияти Шӯроӣ дар Бухорои Шарқӣ дохил мешавад. Соли 1921 то 1923 чун ваколатдори кумитаи озуқаи ҶХС Бухоро дар таъмини аскарони сурх ширкат карда, ҳамчунин дар муборизаи зидди босмачигарӣ дар Ғарм ва Душанбе иштирок кардааст. Сипас ба ҳайси раиси коммисиюни фавқулодаи мутаадиди Бухорои Шарқӣ, раиси КИМ-и Бухорои Шарқ ва раиси кумитаи инқилоби ҶМШС Тоҷикистон кор кардааст.</p>
