    <h1>Бобоҷон Ғафуров</h1>
    <p>Бобоҷон Ғафуров Қаҳрамони Тоҷикистон, арбоби сиёсию давлатӣ, муаррихи барҷаста, шарқшиноси машҳури тоҷик, доктори илми таърих, профессор, узви пайвастаи АИ Тоҷикистон (1951), узви пайвастаи АИ Иттиҳоди Шӯравӣ (1968), Ходими хизматнишондодаи илми Ҷумҳурии Тоҷикистон (1967). <br>Бобоҷон Ғафуров 31 декабри соли 1908 дар рустои Исфисор, ҳоло ш. Ғафурови вилояти Суғд ба дунё омад. Дар ташаккули ӯ, чун инсони комилу хоксор, ватандӯст, олими нуктасанҷ оилаи меҳнатдӱст, падараш Сангинов Ғафур деҳқон ва коргари роҳи оҳан (соли вафот 1935) ва хусусан модари маърифатпарвари ӯ шоира Розия Озод (Розия Бойматовна Ғафурова) таъсири мусбӣ расонидаанд. Давраи ҷавонии ӯ дар вазъияте мегузашт, ки ҷараёни сиёсӣ-иртиҷоии туркпарастон (пантуркистҳо) вуҷуд доштани халқи тоҷикро инкор карда, Осиёи Миёнаро ба империяи «Туркияи бузург» пайваст карданӣ буданд. Фаъолияти илмӣ-эҷодии Б. Ғафуров дар шароите оғоз гардид, ки дар афкори ҷамъиятӣ бояд исбот мешуд, ки халқи тоҷик бо меҳнати шоёнаш дар тамаддун аз давраҳои қадим маълум ва машҳур аст.</p>
